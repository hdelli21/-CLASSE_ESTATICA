public class ConversorUnidades {

    //Método Estático de conversão de Celcius Pafa Fahrenheit

    public static double celsiusParaFahrenheit(double Celcius) {
        return (Celcius * 9 / 5) + 32; // Formula de conversão

    }
//Método Estático de conversão de Quilometro Para Milhas
    public static double quilometrosParaMilhas(double quilometros) {
        return quilometros * 0.621371; // Formula de conversão
    }
//Método Estático de conversão de Gramas Para Libra

    public static double gramasParaLibras(double gramas) {
        return gramas * 0.00220462; // Formula de conversão
    }
}

